  gross_amount numeric(14,2) not null default 0,
  driver_amount numeric(14,2) not null default 0,
  status text not null default 'completed'
    check (status in ('scheduled', 'in_progress', 'completed', 'cancelled')),
  notes text,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists driver_jobs_driver_date_idx
  on public.driver_jobs(driver_id, job_date desc);
create index if not exists driver_jobs_company_date_idx
  on public.driver_jobs(company_id, job_date desc);

drop trigger if exists driver_jobs_set_updated_at on public.driver_jobs;
create trigger driver_jobs_set_updated_at
before update on public.driver_jobs
for each row execute function public.set_updated_at();

-- ------------------------------------------------------------
-- 4. DRIVER PAYOUT FOUNDATION
-- ------------------------------------------------------------
create table if not exists public.driver_payouts (
  id bigint generated by default as identity primary key,
  driver_id bigint not null references public.drivers(id) on delete cascade,
  company_id bigint not null references public.companies(id) on delete restrict,
  payout_no text unique,
  period_start date,
  period_end date,
  gross_earnings numeric(14,2) not null default 0,
  deductions numeric(14,2) not null default 0,
  advances numeric(14,2) not null default 0,
  amount_paid numeric(14,2) not null default 0,
  net_payout numeric(14,2) generated always as
    (gross_earnings - deductions - advances) stored,
  outstanding_amount numeric(14,2) generated always as
    (greatest(gross_earnings - deductions - advances - amount_paid, 0)) stored,
  payment_date date,
  payment_method text,
  status text not null default 'draft'
    check (status in ('draft', 'approved', 'partial', 'paid', 'cancelled')),
  notes text,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists driver_payouts_driver_period_idx
  on public.driver_payouts(driver_id, period_end desc);
create index if not exists driver_payouts_company_period_idx
  on public.driver_payouts(company_id, period_end desc);

drop trigger if exists driver_payouts_set_updated_at on public.driver_payouts;
create trigger driver_payouts_set_updated_at
before update on public.driver_payouts
for each row execute function public.set_updated_at();

-- ------------------------------------------------------------
-- 5. DRIVER SELF-SERVICE RPC
-- Only the editable fields are accepted by this function.
-- ------------------------------------------------------------
create or replace function public.update_own_driver_profile(
  p_phone text,
  p_contact_email text,
  p_address text,
  p_emergency_contact_name text,
  p_emergency_contact_phone text,
  p_vehicle_make text,
  p_vehicle_model text,
  p_vehicle_plate text,
  p_vehicle_type text,
  p_bank_name text,
  p_bank_account_name text,
  p_bank_account_no text,
  p_paynow_type text,
  p_paynow_no text,
  p_notes text
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  updated_driver_id bigint;
begin
  if auth.uid() is null then
    raise exception 'You must be signed in.';
  end if;

  update public.drivers
  set phone = nullif(btrim(p_phone), ''),
      contact_email = nullif(lower(btrim(p_contact_email)), ''),
      address = nullif(btrim(p_address), ''),
      emergency_contact_name = nullif(btrim(p_emergency_contact_name), ''),
      emergency_contact_phone = nullif(btrim(p_emergency_contact_phone), ''),
      vehicle_make = nullif(btrim(p_vehicle_make), ''),
      vehicle_model = nullif(btrim(p_vehicle_model), ''),
      vehicle_plate = nullif(upper(btrim(p_vehicle_plate)), ''),
      vehicle_type = nullif(btrim(p_vehicle_type), ''),
      bank_name = nullif(btrim(p_bank_name), ''),
      bank_account_name = nullif(btrim(p_bank_account_name), ''),
      bank_account_no = nullif(btrim(p_bank_account_no), ''),
      paynow_type = case
        when nullif(btrim(p_paynow_type), '') is null then null
        when p_paynow_type in ('mobile', 'nric', 'uen', 'other') then p_paynow_type
        else null
      end,
      paynow_no = nullif(btrim(p_paynow_no), ''),
      notes = nullif(btrim(p_notes), ''),
      updated_by = auth.uid(),
      updated_at = now()
  where auth_user_id = auth.uid()
    and status = 'active'
  returning id into updated_driver_id;

  if updated_driver_id is null then
    raise exception 'Active driver profile was not found.';
  end if;

  return updated_driver_id;
end;
$$;

