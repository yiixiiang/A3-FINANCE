begin;

-- ------------------------------------------------------------
-- 1. ALLOW THE DRIVER LOGIN ROLE
-- ------------------------------------------------------------
do $$
declare
  constraint_name text;
begin
  for constraint_name in
    select conname
    from pg_constraint
    where conrelid = 'public.profiles'::regclass
      and contype = 'c'
      and pg_get_constraintdef(oid) ilike '%role%'
  loop
    execute format('alter table public.profiles drop constraint %I', constraint_name);
  end loop;
end
$$;

alter table public.profiles
  add constraint profiles_role_check
  check (role in ('administrator', 'finance', 'viewer', 'user', 'driver'))
  not valid;

alter table public.profiles validate constraint profiles_role_check;

-- ------------------------------------------------------------
-- 2. DRIVER MASTER
-- ------------------------------------------------------------
create table if not exists public.drivers (
  id bigint generated by default as identity primary key,
  driver_no text unique,
  company_id bigint not null references public.companies(id) on delete restrict,
  auth_user_id uuid unique references auth.users(id) on delete set null,

  full_name text not null,
  phone text,
  contact_email text,
  login_email text,
  address text,
  nric_passport text,
  nationality text,
  date_of_birth date,

  licence_no text,
  licence_class text,
  licence_expiry date,

  emergency_contact_name text,
  emergency_contact_phone text,

  vehicle_make text,
  vehicle_model text,
  vehicle_plate text,
  vehicle_type text,

  bank_name text,
  bank_account_name text,
  bank_account_no text,
  paynow_type text check (paynow_type is null or paynow_type in ('mobile', 'nric', 'uen', 'other')),
  paynow_no text,

  profile_photo_path text,
  licence_front_path text,
  licence_back_path text,
  identity_document_path text,

  login_enabled boolean not null default false,
  status text not null default 'active' check (status in ('active', 'inactive')),
  notes text,

  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists drivers_login_email_lower_uidx
  on public.drivers(lower(login_email))
  where login_email is not null;
create index if not exists drivers_company_id_idx on public.drivers(company_id);
create index if not exists drivers_auth_user_id_idx on public.drivers(auth_user_id);
create index if not exists drivers_status_idx on public.drivers(status);
create index if not exists drivers_vehicle_plate_lower_idx on public.drivers(lower(vehicle_plate));

create or replace function public.assign_driver_number()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if new.driver_no is null or btrim(new.driver_no) = '' then
    new.driver_no := 'DRV-' || lpad(new.id::text, 5, '0');
  end if;
  return new;
end;
$$;

drop trigger if exists drivers_assign_number on public.drivers;
create trigger drivers_assign_number
before insert on public.drivers
for each row execute function public.assign_driver_number();

drop trigger if exists drivers_set_updated_at on public.drivers;
create trigger drivers_set_updated_at
before update on public.drivers
for each row execute function public.set_updated_at();

-- ------------------------------------------------------------
-- 3. DRIVER JOB FOUNDATION
-- ------------------------------------------------------------
create table if not exists public.driver_jobs (
  id bigint generated by default as identity primary key,
  driver_id bigint not null references public.drivers(id) on delete cascade,
  company_id bigint not null references public.companies(id) on delete restrict,
  job_reference text,
  job_date date not null default current_date,
  service_type text,
  pickup_location text,
