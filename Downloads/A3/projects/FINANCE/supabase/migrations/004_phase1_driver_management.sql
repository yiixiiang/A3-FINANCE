-- Drivers: administrators manage all; linked driver reads their own record.
drop policy if exists drivers_select_admin_or_self on public.drivers;
create policy drivers_select_admin_or_self on public.drivers
for select to authenticated
using (
  public.is_app_administrator()
  or auth_user_id = auth.uid()
);

drop policy if exists drivers_insert_administrator on public.drivers;
create policy drivers_insert_administrator on public.drivers
for insert to authenticated
with check (public.is_app_administrator());

drop policy if exists drivers_update_administrator on public.drivers;
create policy drivers_update_administrator on public.drivers
for update to authenticated
using (public.is_app_administrator())
with check (public.is_app_administrator());

drop policy if exists drivers_delete_administrator on public.drivers;
create policy drivers_delete_administrator on public.drivers
for delete to authenticated
using (public.is_app_administrator());

-- Jobs: administrators manage; linked drivers can view their own jobs.
drop policy if exists driver_jobs_select_admin_or_self on public.driver_jobs;
create policy driver_jobs_select_admin_or_self on public.driver_jobs
for select to authenticated
using (
  public.is_app_administrator()
  or exists (
    select 1 from public.drivers driver
    where driver.id = driver_jobs.driver_id
      and driver.auth_user_id = auth.uid()
  )
);

drop policy if exists driver_jobs_write_administrator on public.driver_jobs;
create policy driver_jobs_write_administrator on public.driver_jobs
for all to authenticated
using (public.is_app_administrator())
with check (public.is_app_administrator());

-- Payouts: administrators manage; linked drivers can view their own payouts.
drop policy if exists driver_payouts_select_admin_or_self on public.driver_payouts;
create policy driver_payouts_select_admin_or_self on public.driver_payouts
for select to authenticated
using (
  public.is_app_administrator()
  or exists (
    select 1 from public.drivers driver
    where driver.id = driver_payouts.driver_id
      and driver.auth_user_id = auth.uid()
  )
);

drop policy if exists driver_payouts_write_administrator on public.driver_payouts;
create policy driver_payouts_write_administrator on public.driver_payouts
for all to authenticated
using (public.is_app_administrator())
with check (public.is_app_administrator());

