-- 7. PRIVATE DRIVER DOCUMENT STORAGE
-- ------------------------------------------------------------
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'driver-documents',
  'driver-documents',
  false,
  10485760,
  array['image/png', 'image/jpeg', 'image/webp', 'application/pdf']
)
on conflict (id) do update
set public = false,
    file_size_limit = excluded.file_size_limit,
    allowed_mime_types = excluded.allowed_mime_types;

-- Administrators and the linked driver can read a driver's own folder.
drop policy if exists driver_documents_select_admin_or_self on storage.objects;
create policy driver_documents_select_admin_or_self
on storage.objects
for select
to authenticated
using (
  bucket_id = 'driver-documents'
  and (
    public.is_app_administrator()
    or exists (
      select 1 from public.drivers driver
      where driver.auth_user_id = auth.uid()
        and driver.id::text = (storage.foldername(name))[2]
    )
  )
);

drop policy if exists driver_documents_insert_administrator on storage.objects;
create policy driver_documents_insert_administrator
on storage.objects
for insert
to authenticated
with check (
  bucket_id = 'driver-documents'
  and public.is_app_administrator()
);

drop policy if exists driver_documents_update_administrator on storage.objects;
create policy driver_documents_update_administrator
on storage.objects
for update
to authenticated
using (
  bucket_id = 'driver-documents'
  and public.is_app_administrator()
)
with check (
  bucket_id = 'driver-documents'
  and public.is_app_administrator()
);

drop policy if exists driver_documents_delete_administrator on storage.objects;
create policy driver_documents_delete_administrator
on storage.objects
for delete
to authenticated
using (
  bucket_id = 'driver-documents'
  and public.is_app_administrator()
);

