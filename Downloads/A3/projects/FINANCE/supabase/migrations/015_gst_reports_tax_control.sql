-- 5. PERIOD REFRESH / SNAPSHOT ENGINE
-- -----------------------------------------------------------------------------
create or replace function public.refresh_gst_filing_period(p_period_id bigint)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_period public.gst_filing_periods%rowtype;
  v_box1 numeric(14,2) := 0;
  v_box2 numeric(14,2) := 0;
  v_box3 numeric(14,2) := 0;
  v_box4 numeric(14,2) := 0;
  v_box5 numeric(14,2) := 0;
  v_box6 numeric(14,2) := 0;
  v_box7 numeric(14,2) := 0;
  v_box8 numeric(14,2) := 0;
begin
  if auth.uid() is null or not public.is_app_administrator() then
    raise exception 'Administrator access is required.';
  end if;

  select * into v_period
  from public.gst_filing_periods
  where id = p_period_id
  for update;

  if not found then
    raise exception 'GST filing period was not found.';
  end if;
  if v_period.status = 'filed' then
    raise exception 'Reopen the filed period before refreshing it.';
  end if;

  delete from public.gst_period_entries where period_id = p_period_id;

  insert into public.gst_period_entries(
    period_id, company_id, source_type, source_id, source_no, source_date,
    counterparty, direction, treatment, taxable_amount, gst_amount,
    recoverable_gst_amount, box_no, description, details
  )
  select v_period.id, invoice.company_id, 'customer_invoice', invoice.id,
         invoice.invoice_no, invoice.invoice_date, invoice.customer_name,
         'output', invoice.gst_treatment,
         round(greatest(coalesce(invoice.subtotal, 0) + coalesce(invoice.service_charge_amount, 0), 0), 2),
         round(greatest(coalesce(invoice.gst_amount, 0), 0), 2),
         0,
         case invoice.gst_treatment
           when 'standard_rated' then 1
           when 'zero_rated' then 2
           when 'exempt' then 3
           else null
         end,
         'Customer invoice',
         jsonb_build_object('status', invoice.status, 'gst_rate', invoice.gst_rate, 'review_status', invoice.gst_review_status)
  from public.customer_invoices invoice
  where invoice.company_id = v_period.company_id
    and invoice.invoice_date between v_period.period_from and v_period.period_to
    and invoice.status not in ('draft','cancelled')
    and coalesce(invoice.gst_reportable, true) = true
    and coalesce(invoice.gst_review_status, 'pending') <> 'excluded';

  insert into public.gst_period_entries(
    period_id, company_id, source_type, source_id, source_no, source_date,
    counterparty, direction, treatment, taxable_amount, gst_amount,
    recoverable_gst_amount, box_no, description, details
  )
  select v_period.id, bill.company_id, 'supplier_bill', bill.id,
         bill.bill_no, bill.bill_date, bill.supplier_name,
         'input', bill.gst_treatment,
         round(greatest(coalesce(bill.subtotal, 0), 0), 2),
         round(greatest(coalesce(bill.gst_amount, 0), 0), 2),
         round(greatest(coalesce(bill.recoverable_gst_amount, 0), 0), 2),
         case when bill.gst_treatment in ('standard_rated','blocked_input') then 5 else null end,
         'Supplier bill',
         jsonb_build_object('status', bill.status, 'recoverable_percentage', bill.input_tax_recoverable_percentage, 'review_status', bill.gst_review_status)
  from public.supplier_bills bill
  where bill.company_id = v_period.company_id
    and bill.bill_date between v_period.period_from and v_period.period_to
    and bill.status not in ('draft','cancelled')
    and coalesce(bill.gst_reportable, true) = true
    and coalesce(bill.gst_review_status, 'pending') <> 'excluded';

  insert into public.gst_period_entries(
    period_id, company_id, source_type, source_id, source_no, source_date,
    counterparty, direction, treatment, taxable_amount, gst_amount,
    recoverable_gst_amount, box_no, description, details
  )
  select v_period.id, adjustment.company_id, 'adjustment', adjustment.id,
         adjustment.adjustment_no, adjustment.adjustment_date, null,
         case when adjustment.adjustment_type = 'input_tax' then 'input' else 'output' end,
         adjustment.treatment,
         round(coalesce(adjustment.taxable_amount, 0), 2),
         round(coalesce(adjustment.gst_amount, 0), 2),
         case when adjustment.adjustment_type = 'input_tax' then round(coalesce(adjustment.gst_amount, 0), 2) else 0 end,
         case
           when adjustment.adjustment_type = 'input_tax' then 5
           when adjustment.treatment = 'standard_rated' then 1
           when adjustment.treatment = 'zero_rated' then 2
           when adjustment.treatment = 'exempt' then 3
           else null
         end,
         adjustment.reason,
         jsonb_build_object('reference', adjustment.reference, 'notes', adjustment.notes)
  from public.gst_adjustments adjustment
  where adjustment.company_id = v_period.company_id
    and adjustment.adjustment_date between v_period.period_from and v_period.period_to
    and adjustment.status = 'posted';

  select
    coalesce(sum(case when direction = 'output' and treatment = 'standard_rated' then taxable_amount else 0 end), 0),
    coalesce(sum(case when direction = 'output' and treatment = 'zero_rated' then taxable_amount else 0 end), 0),
    coalesce(sum(case when direction = 'output' and treatment = 'exempt' then taxable_amount else 0 end), 0),
    coalesce(sum(case when direction = 'input' and treatment in ('standard_rated','blocked_input') then taxable_amount else 0 end), 0),
    coalesce(sum(case when direction = 'output' then gst_amount else 0 end), 0),
    coalesce(sum(case when direction = 'input' then recoverable_gst_amount else 0 end), 0)
  into v_box1, v_box2, v_box3, v_box5, v_box6, v_box7
  from public.gst_period_entries
  where period_id = p_period_id;

  v_box4 := round(v_box1 + v_box2 + v_box3, 2);
  v_box8 := round(v_box6 - v_box7, 2);

  update public.gst_filing_periods
  set box1_standard_rated_supplies = round(v_box1, 2),
      box2_zero_rated_supplies = round(v_box2, 2),
      box3_exempt_supplies = round(v_box3, 2),
      box4_total_supplies = round(v_box4, 2),
      box5_taxable_purchases = round(v_box5, 2),
      box6_output_tax = round(v_box6, 2),
      box7_input_tax = round(v_box7, 2),
      box8_net_gst = round(v_box8, 2),
      source_snapshot_at = now(),
      updated_at = now()
  where id = p_period_id;

  return jsonb_build_object(
    'period_id', p_period_id,
    'box1', round(v_box1, 2), 'box2', round(v_box2, 2),
    'box3', round(v_box3, 2), 'box4', round(v_box4, 2),
    'box5', round(v_box5, 2), 'box6', round(v_box6, 2),
    'box7', round(v_box7, 2), 'box8', round(v_box8, 2)
  );
end;
$$;

