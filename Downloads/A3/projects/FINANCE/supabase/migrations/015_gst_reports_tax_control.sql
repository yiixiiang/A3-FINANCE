create or replace function public.prepare_supplier_bill_gst_control()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.input_tax_recoverable_percentage := least(greatest(coalesce(new.input_tax_recoverable_percentage, 100), 0), 100);
  new.recoverable_gst_amount := round(greatest(coalesce(new.gst_amount, 0), 0) * new.input_tax_recoverable_percentage / 100, 2);
  new.gst_reportable := coalesce(new.gst_reportable, true);
  new.gst_review_status := case when lower(coalesce(new.gst_review_status, 'pending')) in ('pending','reviewed','excluded') then lower(new.gst_review_status) else 'pending' end;
  return new;
end;
$$;

