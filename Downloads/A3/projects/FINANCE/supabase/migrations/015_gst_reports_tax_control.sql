-- -----------------------------------------------------------------------------
-- 2. GST TAX CODES
-- -----------------------------------------------------------------------------
create table if not exists public.gst_tax_codes (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  code text not null,
  name text not null,
  transaction_type text not null default 'both',
  treatment text not null default 'standard_rated',
  rate numeric(7,4) not null default 0,
  recoverable_percentage numeric(7,4) not null default 100,
  box_no integer,
  is_default_sales boolean not null default false,
  is_default_purchase boolean not null default false,
  is_active boolean not null default true,
  sort_order integer not null default 100,
  description text,
  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (transaction_type in ('sales','purchase','both')),
  check (treatment in ('standard_rated','zero_rated','exempt','out_of_scope','blocked_input')),
  check (rate between 0 and 100),
  check (recoverable_percentage between 0 and 100),
  check (box_no is null or box_no between 1 and 8)
);

alter table public.gst_tax_codes
  add column if not exists company_id bigint references public.companies(id) on delete cascade,
  add column if not exists code text,
  add column if not exists name text,
  add column if not exists transaction_type text default 'both',
  add column if not exists treatment text default 'standard_rated',
  add column if not exists rate numeric(7,4) default 0,
  add column if not exists recoverable_percentage numeric(7,4) default 100,
  add column if not exists box_no integer,
  add column if not exists is_default_sales boolean default false,
  add column if not exists is_default_purchase boolean default false,
  add column if not exists is_active boolean default true,
  add column if not exists sort_order integer default 100,
  add column if not exists description text,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists updated_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

update public.gst_tax_codes
set code = upper(coalesce(nullif(btrim(code), ''), 'GST-' || id::text)),
    name = coalesce(nullif(btrim(name), ''), 'GST Code'),
    transaction_type = case when lower(coalesce(transaction_type, 'both')) in ('sales','purchase','both') then lower(transaction_type) else 'both' end,
    treatment = case when lower(coalesce(treatment, 'standard_rated')) in ('standard_rated','zero_rated','exempt','out_of_scope','blocked_input') then lower(treatment) else 'standard_rated' end,
    rate = least(greatest(coalesce(rate, 0), 0), 100),
    recoverable_percentage = least(greatest(coalesce(recoverable_percentage, 100), 0), 100),
    is_default_sales = coalesce(is_default_sales, false),
    is_default_purchase = coalesce(is_default_purchase, false),
    is_active = coalesce(is_active, true),
    sort_order = coalesce(sort_order, 100),
    created_at = coalesce(created_at, now()),
    updated_at = coalesce(updated_at, now());

-- Normalise duplicate legacy codes before enforcing uniqueness.
update public.gst_tax_codes duplicate
set code = duplicate.code || '-DUP-' || duplicate.id::text
where exists (
  select 1 from public.gst_tax_codes original
  where original.company_id = duplicate.company_id
    and lower(original.code) = lower(duplicate.code)
    and original.id < duplicate.id
);

-- Keep only the first legacy default in each direction.
update public.gst_tax_codes duplicate
set is_default_sales = false
where duplicate.is_default_sales = true
  and exists (
    select 1 from public.gst_tax_codes original
    where original.company_id = duplicate.company_id
      and original.is_default_sales = true
      and original.id < duplicate.id
  );

update public.gst_tax_codes duplicate
set is_default_purchase = false
where duplicate.is_default_purchase = true
  and exists (
    select 1 from public.gst_tax_codes original
    where original.company_id = duplicate.company_id
      and original.is_default_purchase = true
      and original.id < duplicate.id
  );

create unique index if not exists gst_tax_codes_company_code_uidx
  on public.gst_tax_codes(company_id, lower(code));
create index if not exists gst_tax_codes_company_active_idx
  on public.gst_tax_codes(company_id, is_active, sort_order, code);
create unique index if not exists gst_tax_codes_default_sales_uidx
  on public.gst_tax_codes(company_id)
  where is_default_sales = true;
create unique index if not exists gst_tax_codes_default_purchase_uidx
  on public.gst_tax_codes(company_id)
  where is_default_purchase = true;

create or replace function public.prepare_gst_tax_code()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.code := upper(coalesce(nullif(btrim(new.code), ''), 'GST'));
  new.name := coalesce(nullif(btrim(new.name), ''), new.code);
  new.transaction_type := case when lower(coalesce(new.transaction_type, 'both')) in ('sales','purchase','both') then lower(new.transaction_type) else 'both' end;
  new.treatment := case when lower(coalesce(new.treatment, 'standard_rated')) in ('standard_rated','zero_rated','exempt','out_of_scope','blocked_input') then lower(new.treatment) else 'standard_rated' end;
  new.rate := least(greatest(coalesce(new.rate, 0), 0), 100);
  new.recoverable_percentage := least(greatest(coalesce(new.recoverable_percentage, 100), 0), 100);
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists gst_tax_codes_prepare_record on public.gst_tax_codes;
create trigger gst_tax_codes_prepare_record
before insert or update on public.gst_tax_codes
for each row execute function public.prepare_gst_tax_code();

-- Seed a complete starter set for every company.
insert into public.gst_tax_codes(
  company_id, code, name, transaction_type, treatment, rate,
  recoverable_percentage, box_no, is_default_sales, is_default_purchase,
  sort_order, description
)
select company.id, seed.code, seed.name, seed.transaction_type, seed.treatment,
       case when seed.use_company_rate then coalesce(company.gst_rate, 9) else seed.rate end,
       seed.recoverable_percentage, seed.box_no,
       seed.is_default_sales, seed.is_default_purchase, seed.sort_order, seed.description
from public.companies company
cross join (
  values
    ('SR-S', 'Standard-Rated Sales', 'sales', 'standard_rated', 0::numeric, true, 100::numeric, 1, true, false, 10, 'Taxable local sales at the company GST rate.'),
    ('ZR-S', 'Zero-Rated Sales', 'sales', 'zero_rated', 0::numeric, false, 100::numeric, 2, false, false, 20, 'Zero-rated supplies.'),
    ('ES-S', 'Exempt Sales', 'sales', 'exempt', 0::numeric, false, 100::numeric, 3, false, false, 30, 'Exempt supplies.'),
    ('OS-S', 'Out-of-Scope Sales', 'sales', 'out_of_scope', 0::numeric, false, 100::numeric, null::integer, false, false, 40, 'Transactions outside the GST return.'),
    ('TX-P', 'Taxable Purchase', 'purchase', 'standard_rated', 0::numeric, true, 100::numeric, 5, false, true, 50, 'Purchase with recoverable input tax.'),
    ('BL-P', 'Blocked Input Tax', 'purchase', 'blocked_input', 0::numeric, true, 0::numeric, 5, false, false, 60, 'Purchase where input tax is not recoverable.'),
    ('NR-P', 'Non-Taxable Purchase', 'purchase', 'out_of_scope', 0::numeric, false, 0::numeric, null::integer, false, false, 70, 'Purchase without claimable GST.')
) as seed(code,name,transaction_type,treatment,rate,use_company_rate,recoverable_percentage,box_no,is_default_sales,is_default_purchase,sort_order,description)
on conflict do nothing;

