create or replace function public.seed_cash_flow_defaults(p_company_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.cash_flow_mappings(company_id, source_type, entry_type, activity, line_name, priority, notes)
  values
    (p_company_id, 'customer_payment', 'receipt', 'operating', 'Receipts from customers', 10, 'Posted customer receipts.'),
    (p_company_id, 'supplier_payment', 'payment', 'operating', 'Payments to suppliers', 20, 'Posted supplier payments.'),
    (p_company_id, 'driver_payout', 'payment', 'operating', 'Payments to drivers', 30, 'Driver payout settlement.'),
    (p_company_id, '*', 'bank_fee', 'operating', 'Bank and payment charges', 40, 'Bank charges and processing fees.'),
    (p_company_id, '*', 'interest', 'operating', 'Interest received or paid', 50, 'Interest cash movement.'),
    (p_company_id, '*', 'transfer', 'excluded', 'Transfers between own accounts', 60, 'Excluded from the cash-flow statement to avoid double counting.'),
    (p_company_id, '*', 'receipt', 'operating', 'Other operating receipts', 70, 'Other cash receipts.'),
    (p_company_id, '*', 'payment', 'operating', 'Other operating payments', 80, 'Other cash payments.'),
    (p_company_id, '*', 'adjustment', 'operating', 'Other operating cash movements', 90, 'Manual cash-book adjustment.')
  on conflict (company_id, source_type, entry_type) do update
    set activity = excluded.activity,
        line_name = excluded.line_name,
        priority = excluded.priority,
        notes = excluded.notes,
        updated_at = now();
end;
$$;

do $$ declare company_record record; begin
  for company_record in select id from public.companies loop
    perform public.seed_cash_flow_defaults(company_record.id);
  end loop;
end $$;

create or replace function public.seed_cash_flow_defaults_for_new_company()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  perform public.seed_cash_flow_defaults(new.id);
  return new;
end;
$$;

drop trigger if exists companies_seed_cash_flow_defaults on public.companies;
create trigger companies_seed_cash_flow_defaults
after insert on public.companies
for each row execute function public.seed_cash_flow_defaults_for_new_company();

