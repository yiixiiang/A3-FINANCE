create or replace function public.assign_customer_payment_receipt_no()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if new.receipt_no is null or btrim(new.receipt_no) = '' then
    new.receipt_no := 'RCPT-' ||
      to_char(coalesce(new.payment_date, current_date), 'YYYYMMDD') || '-' ||
      lpad(new.id::text, 5, '0');
  end if;
  new.currency := upper(coalesce(nullif(btrim(new.currency), ''), 'SGD'));
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists customer_payments_assign_receipt_no on public.customer_payments;
create trigger customer_payments_assign_receipt_no
before insert or update on public.customer_payments
for each row execute function public.assign_customer_payment_receipt_no();

create or replace function public.refresh_customer_invoice_payment_state(p_invoice_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_invoice public.customer_invoices%rowtype;
  v_allocated numeric(14,2) := 0;
  v_paid numeric(14,2) := 0;
  v_status text;
begin
  select * into v_invoice
  from public.customer_invoices
  where id = p_invoice_id
  for update;

  if not found then
    return;
  end if;

  select coalesce(sum(allocation.allocated_amount), 0)
  into v_allocated
  from public.customer_payment_allocations allocation
  join public.customer_payments payment on payment.id = allocation.payment_id
  where allocation.invoice_id = p_invoice_id
    and payment.status = 'posted';

  v_paid := least(
    greatest(coalesce(v_invoice.total_amount, 0), 0),
    greatest(coalesce(v_invoice.payment_ledger_opening_amount, 0), 0) + greatest(v_allocated, 0)
  );

  if v_invoice.status = 'cancelled' then
    v_status := 'cancelled';
  elsif v_paid >= coalesce(v_invoice.total_amount, 0) and coalesce(v_invoice.total_amount, 0) > 0 then
    v_status := 'paid';
  elsif v_paid > 0 then
    v_status := 'partial';
  elsif v_invoice.due_date is not null and v_invoice.due_date < current_date then
    v_status := 'overdue';
  elsif v_invoice.status = 'draft' then
    v_status := 'draft';
  else
    v_status := 'issued';
  end if;

  update public.customer_invoices
  set amount_paid = round(v_paid, 2),
      status = v_status,
      updated_at = now()
  where id = p_invoice_id;
end;
$$;

create or replace function public.protect_customer_invoice_ledger_amount()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_allocated numeric(14,2) := 0;
  v_has_allocations boolean := false;
begin
  if tg_op = 'INSERT' then
    new.payment_ledger_opening_amount := greatest(coalesce(new.amount_paid, 0), 0);
    return new;
  end if;

  select exists (
    select 1
    from public.customer_payment_allocations allocation
    join public.customer_payments payment on payment.id = allocation.payment_id
    where allocation.invoice_id = old.id
      and payment.status = 'posted'
  ) into v_has_allocations;

  if not v_has_allocations then
    if new.amount_paid is distinct from old.amount_paid then
      new.payment_ledger_opening_amount := greatest(coalesce(new.amount_paid, 0), 0);
    end if;
    return new;
  end if;

  select coalesce(sum(allocation.allocated_amount), 0)
  into v_allocated
  from public.customer_payment_allocations allocation
  join public.customer_payments payment on payment.id = allocation.payment_id
  where allocation.invoice_id = old.id
    and payment.status = 'posted';

  new.amount_paid := least(
    greatest(coalesce(new.total_amount, 0), 0),
    greatest(coalesce(old.payment_ledger_opening_amount, 0), 0) + greatest(v_allocated, 0)
  );

  if old.status = 'cancelled' or new.status = 'cancelled' then
    new.status := 'cancelled';
  elsif new.amount_paid >= coalesce(new.total_amount, 0) and coalesce(new.total_amount, 0) > 0 then
    new.status := 'paid';
  elsif new.amount_paid > 0 then
    new.status := 'partial';
  elsif new.due_date is not null and new.due_date < current_date then
    new.status := 'overdue';
  elsif old.status = 'draft' and new.status = 'draft' then
    new.status := 'draft';
  else
    new.status := 'issued';
  end if;

  return new;
end;
$$;

drop trigger if exists customer_invoices_protect_ledger_amount on public.customer_invoices;
create trigger customer_invoices_protect_ledger_amount
before insert or update of amount_paid, total_amount, due_date, status
on public.customer_invoices
for each row execute function public.protect_customer_invoice_ledger_amount();

create or replace function public.record_customer_payment(
  p_company_id bigint,
  p_customer_id bigint,
  p_customer_name text,
  p_payment_date date,
  p_currency text,
  p_amount numeric,
  p_payment_method text,
  p_payment_reference text default null,
  p_notes text default null,
  p_allocations jsonb default '[]'::jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_payment_id bigint;
  v_customer_name text := nullif(btrim(coalesce(p_customer_name, '')), '');
  v_customer public.customers%rowtype;
  v_allocation jsonb;
  v_invoice public.customer_invoices%rowtype;
  v_invoice_id bigint;
  v_amount numeric(14,2);
  v_allocated numeric(14,2) := 0;
  v_balance numeric(14,2);
begin
  if v_user_id is null then
    raise exception 'Authentication is required.';
  end if;
  if not public.is_app_administrator() then
    raise exception 'Administrator access is required.';
  end if;
  if p_company_id is null or not exists (
    select 1 from public.companies where id = p_company_id
  ) then
    raise exception 'A valid company is required.';
  end if;
  if coalesce(p_amount, 0) <= 0 then
    raise exception 'Payment amount must be greater than zero.';
  end if;
  if nullif(btrim(coalesce(p_payment_method, '')), '') is null then
    raise exception 'Payment method is required.';
  end if;
  if jsonb_typeof(coalesce(p_allocations, '[]'::jsonb)) <> 'array' then
    raise exception 'Payment allocations must be an array.';
  end if;

  if p_customer_id is not null then
    select * into v_customer
    from public.customers
    where id = p_customer_id
      and company_id = p_company_id;
    if not found then
      raise exception 'The selected customer does not belong to this company.';
    end if;
    v_customer_name := v_customer.customer_name;
  end if;

  if v_customer_name is null then
    raise exception 'Customer name is required.';
  end if;

  insert into public.customer_payments (
    company_id,
    customer_id,
    customer_name,
    payment_date,
    currency,
    amount,
    allocated_amount,
    unallocated_amount,
    payment_method,
    payment_reference,
    notes,
    created_by
  ) values (
    p_company_id,
    p_customer_id,
    v_customer_name,
    coalesce(p_payment_date, current_date),
    upper(coalesce(nullif(btrim(p_currency), ''), 'SGD')),
    round(p_amount, 2),
    0,
    round(p_amount, 2),
    btrim(p_payment_method),
    nullif(btrim(coalesce(p_payment_reference, '')), ''),
    nullif(btrim(coalesce(p_notes, '')), ''),
    v_user_id
  ) returning id into v_payment_id;

  for v_allocation in
    select value from jsonb_array_elements(coalesce(p_allocations, '[]'::jsonb))
  loop
    begin
      v_invoice_id := (v_allocation ->> 'invoice_id')::bigint;
      v_amount := round((v_allocation ->> 'amount')::numeric, 2);
    exception when others then
      raise exception 'Every allocation requires a valid invoice and amount.';
    end;

    if v_amount <= 0 then
      raise exception 'Every allocation amount must be greater than zero.';
    end if;

    select * into v_invoice
    from public.customer_invoices
    where id = v_invoice_id
    for update;

    if not found then
      raise exception 'Invoice % was not found.', v_invoice_id;
    end if;
    if v_invoice.company_id <> p_company_id then
      raise exception 'Invoice % belongs to another company.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;
    if upper(coalesce(v_invoice.currency, 'SGD')) <> upper(coalesce(p_currency, 'SGD')) then
      raise exception 'Invoice % uses a different currency.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;
    if v_invoice.status = 'cancelled' then
      raise exception 'Cancelled invoice % cannot receive payment.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;
    if p_customer_id is not null and v_invoice.customer_id is not null and v_invoice.customer_id <> p_customer_id then
      raise exception 'Invoice % belongs to another customer.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;

    v_balance := greatest(coalesce(v_invoice.total_amount, 0) - coalesce(v_invoice.amount_paid, 0), 0);
    if v_amount > v_balance then
      raise exception 'Allocation for invoice % exceeds its outstanding balance of %.',
        coalesce(v_invoice.invoice_no, v_invoice.id::text),
        to_char(v_balance, 'FM9999999990.00');
    end if;

    v_allocated := v_allocated + v_amount;
    if v_allocated > round(p_amount, 2) then
      raise exception 'Total invoice allocations exceed the payment amount.';
    end if;

    insert into public.customer_payment_allocations(payment_id, invoice_id, allocated_amount)
    values (v_payment_id, v_invoice_id, v_amount);
  end loop;

  update public.customer_payments
  set allocated_amount = round(v_allocated, 2),
      unallocated_amount = round(p_amount - v_allocated, 2),
      updated_at = now()
  where id = v_payment_id;

  for v_invoice_id in
    select distinct invoice_id
    from public.customer_payment_allocations
    where payment_id = v_payment_id
  loop
    perform public.refresh_customer_invoice_payment_state(v_invoice_id);
  end loop;

  return (
    select jsonb_build_object(
      'success', true,
      'payment_id', payment.id,
      'receipt_no', payment.receipt_no,
      'amount', payment.amount,
      'allocated_amount', payment.allocated_amount,
      'unallocated_amount', payment.unallocated_amount
    )
    from public.customer_payments payment
    where payment.id = v_payment_id
  );
end;
$$;

create or replace function public.allocate_customer_payment_credit(
  p_payment_id bigint,
  p_allocations jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_payment public.customer_payments%rowtype;
  v_allocation jsonb;
  v_invoice public.customer_invoices%rowtype;
  v_invoice_id bigint;
  v_amount numeric(14,2);
  v_applied numeric(14,2) := 0;
  v_balance numeric(14,2);
begin
  if v_user_id is null then
    raise exception 'Authentication is required.';
  end if;
  if not public.is_app_administrator() then
    raise exception 'Administrator access is required.';
  end if;
  if jsonb_typeof(coalesce(p_allocations, '[]'::jsonb)) <> 'array'
     or jsonb_array_length(coalesce(p_allocations, '[]'::jsonb)) = 0 then
    raise exception 'Select at least one invoice allocation.';
  end if;

  select * into v_payment
  from public.customer_payments
  where id = p_payment_id
  for update;

  if not found then
    raise exception 'Payment receipt was not found.';
  end if;
  if v_payment.status <> 'posted' then
    raise exception 'Only a posted payment can apply customer credit.';
  end if;
  if v_payment.unallocated_amount <= 0 then
    raise exception 'This payment has no unallocated customer credit.';
  end if;

  for v_allocation in
    select value from jsonb_array_elements(p_allocations)
  loop
    begin
      v_invoice_id := (v_allocation ->> 'invoice_id')::bigint;
      v_amount := round((v_allocation ->> 'amount')::numeric, 2);
    exception when others then
      raise exception 'Every allocation requires a valid invoice and amount.';
    end;

    if v_amount <= 0 then
      raise exception 'Every allocation amount must be greater than zero.';
    end if;

    select * into v_invoice
    from public.customer_invoices
    where id = v_invoice_id
    for update;

    if not found then
      raise exception 'Invoice % was not found.', v_invoice_id;
    end if;
    if v_invoice.company_id <> v_payment.company_id then
      raise exception 'Invoice % belongs to another company.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;
    if upper(coalesce(v_invoice.currency, 'SGD')) <> upper(v_payment.currency) then
      raise exception 'Invoice % uses a different currency.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;
    if v_invoice.status = 'cancelled' then
      raise exception 'Cancelled invoice % cannot receive payment.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;
    if v_payment.customer_id is not null
       and v_invoice.customer_id is not null
       and v_invoice.customer_id <> v_payment.customer_id then
      raise exception 'Invoice % belongs to another customer.', coalesce(v_invoice.invoice_no, v_invoice.id::text);
    end if;

    v_balance := greatest(coalesce(v_invoice.total_amount, 0) - coalesce(v_invoice.amount_paid, 0), 0);
    if v_amount > v_balance then
      raise exception 'Allocation for invoice % exceeds its outstanding balance of %.',
        coalesce(v_invoice.invoice_no, v_invoice.id::text),
        to_char(v_balance, 'FM9999999990.00');
    end if;

    v_applied := v_applied + v_amount;
    if v_applied > v_payment.unallocated_amount then
      raise exception 'Allocations exceed the available customer credit of %.',
        to_char(v_payment.unallocated_amount, 'FM9999999990.00');
    end if;

    insert into public.customer_payment_allocations(payment_id, invoice_id, allocated_amount)
    values (v_payment.id, v_invoice_id, v_amount)
    on conflict(payment_id, invoice_id)
    do update set allocated_amount = public.customer_payment_allocations.allocated_amount + excluded.allocated_amount;
  end loop;

  update public.customer_payments
  set allocated_amount = round(allocated_amount + v_applied, 2),
      unallocated_amount = round(unallocated_amount - v_applied, 2),
      updated_at = now()
  where id = v_payment.id;

  for v_invoice_id in
    select distinct invoice_id
    from public.customer_payment_allocations
    where payment_id = v_payment.id
  loop
    perform public.refresh_customer_invoice_payment_state(v_invoice_id);
  end loop;

  return (
    select jsonb_build_object(
      'success', true,
      'payment_id', payment.id,
      'receipt_no', payment.receipt_no,
      'allocated_amount', payment.allocated_amount,
      'unallocated_amount', payment.unallocated_amount
    )
    from public.customer_payments payment
    where payment.id = v_payment.id
  );
end;
$$;

create or replace function public.void_customer_payment(
  p_payment_id bigint,
  p_reason text
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_payment public.customer_payments%rowtype;
  v_invoice_id bigint;
begin
  if v_user_id is null then
    raise exception 'Authentication is required.';
  end if;
  if not public.is_app_administrator() then
    raise exception 'Administrator access is required.';
  end if;
  if nullif(btrim(coalesce(p_reason, '')), '') is null then
    raise exception 'A reversal reason is required.';
  end if;

  select * into v_payment
  from public.customer_payments
  where id = p_payment_id
  for update;

  if not found then
    raise exception 'Payment receipt was not found.';
  end if;
  if v_payment.status = 'void' then
    return jsonb_build_object(
      'success', true,
      'payment_id', v_payment.id,
      'receipt_no', v_payment.receipt_no,
      'status', 'void'
    );
  end if;

  update public.customer_payments
  set status = 'void',
      void_reason = btrim(p_reason),
      voided_by = v_user_id,
      voided_at = now(),
      updated_at = now()
  where id = p_payment_id;

  for v_invoice_id in
    select distinct invoice_id
    from public.customer_payment_allocations
    where payment_id = p_payment_id
  loop
    perform public.refresh_customer_invoice_payment_state(v_invoice_id);
  end loop;

  return jsonb_build_object(
    'success', true,
    'payment_id', v_payment.id,
    'receipt_no', v_payment.receipt_no,
    'status', 'void'
  );
end;
$$;

