alter table public.customer_invoices
  add column if not exists customer_id bigint references public.customers(id) on delete set null,
  add column if not exists currency text not null default 'SGD',
  add column if not exists payment_ledger_opening_amount numeric(14,2) not null default 0;

update public.customer_invoices
set currency = upper(coalesce(nullif(btrim(currency), ''), 'SGD'));

alter table public.customer_invoices
  alter column currency set default 'SGD',
  alter column currency set not null,
  alter column payment_ledger_opening_amount set default 0,
  alter column payment_ledger_opening_amount set not null;

create table if not exists public.customer_payments (
  id bigint generated by default as identity primary key,
  receipt_no text,
  company_id bigint not null references public.companies(id) on delete restrict,
  customer_id bigint references public.customers(id) on delete set null,
  customer_name text not null,
  payment_date date not null default current_date,
  currency text not null default 'SGD',
  amount numeric(14,2) not null check (amount > 0),
  allocated_amount numeric(14,2) not null default 0 check (allocated_amount >= 0),
  unallocated_amount numeric(14,2) not null default 0 check (unallocated_amount >= 0),
  payment_method text not null,
  payment_reference text,
  status text not null default 'posted' check (status in ('posted','void')),
  notes text,
  void_reason text,
  voided_by uuid references public.profiles(id) on delete set null,
  voided_at timestamptz,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (currency ~ '^[A-Z]{3}$'),
  check (allocated_amount + unallocated_amount = amount)
);

-- Schema-safe compatibility for partial or earlier payment tables.
alter table public.customer_payments
  add column if not exists receipt_no text,
  add column if not exists company_id bigint references public.companies(id) on delete restrict,
  add column if not exists customer_id bigint references public.customers(id) on delete set null,
  add column if not exists customer_name text,
  add column if not exists payment_date date default current_date,
  add column if not exists currency text default 'SGD',
  add column if not exists amount numeric(14,2) default 0,
  add column if not exists allocated_amount numeric(14,2) default 0,
  add column if not exists unallocated_amount numeric(14,2) default 0,
  add column if not exists payment_method text,
  add column if not exists payment_reference text,
  add column if not exists status text default 'posted',
  add column if not exists notes text,
  add column if not exists void_reason text,
  add column if not exists voided_by uuid references public.profiles(id) on delete set null,
  add column if not exists voided_at timestamptz,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

update public.customer_payments
set customer_name = coalesce(nullif(btrim(customer_name), ''), 'Customer'),
    payment_date = coalesce(payment_date, current_date),
    currency = upper(coalesce(nullif(btrim(currency), ''), 'SGD')),
    amount = greatest(coalesce(amount, 0), 0),
    allocated_amount = greatest(coalesce(allocated_amount, 0), 0),
    unallocated_amount = greatest(coalesce(unallocated_amount, amount - allocated_amount), 0),
    payment_method = coalesce(nullif(btrim(payment_method), ''), 'Other'),
    status = case when lower(coalesce(status, 'posted')) = 'void' then 'void' else 'posted' end,
    created_at = coalesce(created_at, now()),
    updated_at = coalesce(updated_at, now());

create table if not exists public.customer_payment_allocations (
  id bigint generated by default as identity primary key,
  payment_id bigint not null references public.customer_payments(id) on delete cascade,
  invoice_id bigint not null references public.customer_invoices(id) on delete restrict,
  allocated_amount numeric(14,2) not null check (allocated_amount > 0),
  created_at timestamptz not null default now(),
  unique(payment_id, invoice_id)
);

alter table public.customer_payment_allocations
  add column if not exists payment_id bigint references public.customer_payments(id) on delete cascade,
  add column if not exists invoice_id bigint references public.customer_invoices(id) on delete restrict,
  add column if not exists allocated_amount numeric(14,2) default 0,
  add column if not exists created_at timestamptz default now();

update public.customer_payment_allocations
set allocated_amount = greatest(coalesce(allocated_amount, 0), 0),
    created_at = coalesce(created_at, now());

-- Preserve pre-upgrade amount-paid values exactly once as the opening ledger
-- balance. Do not copy ledger-calculated amounts again when this migration is rerun.
update public.customer_invoices invoice
set payment_ledger_opening_amount = greatest(coalesce(invoice.amount_paid, 0), 0)
where coalesce(invoice.payment_ledger_opening_amount, 0) = 0
  and coalesce(invoice.amount_paid, 0) > 0
  and not exists (
    select 1
    from public.customer_payment_allocations allocation
    where allocation.invoice_id = invoice.id
  );

