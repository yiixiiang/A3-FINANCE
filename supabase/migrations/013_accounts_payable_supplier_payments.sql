create or replace function public.prepare_supplier_record()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.supplier_name := coalesce(nullif(btrim(new.supplier_name), ''), 'Supplier');
  new.default_currency := upper(coalesce(nullif(btrim(new.default_currency), ''), 'SGD'));
  new.payment_terms_days := greatest(coalesce(new.payment_terms_days, 30), 0);
  new.status := case when lower(coalesce(new.status, 'active')) = 'inactive' then 'inactive' else 'active' end;
  if new.supplier_no is null or btrim(new.supplier_no) = '' then
    new.supplier_no := 'SUP-' || lpad(new.id::text, 5, '0');
  end if;
  new.updated_at := now();
  return new;
end;
$$;

