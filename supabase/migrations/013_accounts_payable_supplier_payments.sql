-- -----------------------------------------------------------------------------
create or replace function public.refresh_supplier_bill_state(p_bill_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_bill public.supplier_bills%rowtype;
  v_payment_allocated numeric(14,2) := 0;
  v_credit_allocated numeric(14,2) := 0;
  v_paid numeric(14,2) := 0;
  v_credit numeric(14,2) := 0;
  v_balance numeric(14,2) := 0;
  v_status text;
begin
  select * into v_bill
  from public.supplier_bills
  where id = p_bill_id
  for update;

  if not found then return; end if;

  select coalesce(sum(allocation.allocated_amount), 0)
  into v_payment_allocated
  from public.supplier_payment_allocations allocation
  join public.supplier_payments payment on payment.id = allocation.payment_id
  where allocation.bill_id = p_bill_id and payment.status = 'posted';

  select coalesce(sum(allocation.allocated_amount), 0)
  into v_credit_allocated
  from public.supplier_credit_allocations allocation
  join public.supplier_credit_notes credit on credit.id = allocation.credit_note_id
  where allocation.bill_id = p_bill_id and credit.status = 'posted';

  v_paid := least(
    greatest(coalesce(v_bill.total_amount, 0), 0),
    greatest(coalesce(v_bill.payment_ledger_opening_amount, 0), 0) + greatest(v_payment_allocated, 0)
  );
  v_credit := least(
    greatest(coalesce(v_bill.total_amount, 0) - v_paid, 0),
    greatest(v_credit_allocated, 0)
  );
  v_balance := greatest(coalesce(v_bill.total_amount, 0) - v_paid - v_credit, 0);

  if v_bill.status = 'cancelled' then
    v_status := 'cancelled';
  elsif v_balance <= 0.004 and coalesce(v_bill.total_amount, 0) > 0 then
    v_status := 'paid';
  elsif v_paid + v_credit > 0 then
    v_status := 'partial';
  elsif v_bill.status = 'draft' then
    v_status := 'draft';
  elsif v_bill.due_date is not null and v_bill.due_date < current_date then
    v_status := 'overdue';
  else
    v_status := 'open';
  end if;

  update public.supplier_bills
  set amount_paid = round(v_paid, 2),
      credit_applied = round(v_credit, 2),
      status = v_status,
      updated_at = now()
  where id = p_bill_id;
end;
$$;

-- Preserve any pre-upgrade paid amount once as opening balance.
update public.supplier_bills bill
set payment_ledger_opening_amount = greatest(coalesce(bill.amount_paid, 0), 0)
where coalesce(bill.payment_ledger_opening_amount, 0) = 0
  and coalesce(bill.amount_paid, 0) > 0
  and not exists (
    select 1 from public.supplier_payment_allocations allocation where allocation.bill_id = bill.id
  );

