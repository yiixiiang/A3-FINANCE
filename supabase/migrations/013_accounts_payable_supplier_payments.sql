create or replace function public.record_supplier_payment(
  p_company_id bigint,
  p_supplier_id bigint,
  p_payment_date date,
  p_currency text,
  p_amount numeric,
  p_payment_method text,
  p_payment_reference text,
  p_bank_account text,
  p_notes text,
  p_allocations jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_supplier public.suppliers%rowtype;
  v_payment_id bigint;
  v_allocation jsonb;
  v_bill public.supplier_bills%rowtype;
  v_bill_id bigint;
  v_amount numeric(14,2);
  v_allocated numeric(14,2) := 0;
  v_balance numeric(14,2);
begin
  if v_user_id is null then raise exception 'Authentication is required.'; end if;
  if not public.is_app_administrator() then raise exception 'Administrator access is required.'; end if;
  if p_amount is null or round(p_amount, 2) <= 0 then raise exception 'Payment amount must be greater than zero.'; end if;
  if nullif(btrim(coalesce(p_payment_method, '')), '') is null then raise exception 'Payment method is required.'; end if;

  select * into v_supplier from public.suppliers where id = p_supplier_id and company_id = p_company_id;
  if not found then raise exception 'Supplier was not found for the selected company.'; end if;

  insert into public.supplier_payments(
    company_id, supplier_id, supplier_name, payment_date, currency, amount,
    allocated_amount, unallocated_amount, payment_method, payment_reference,
    bank_account, notes, created_by
  ) values (
    p_company_id, p_supplier_id, v_supplier.supplier_name, coalesce(p_payment_date, current_date),
    upper(coalesce(nullif(btrim(p_currency), ''), v_supplier.default_currency, 'SGD')),
    round(p_amount, 2), 0, round(p_amount, 2), btrim(p_payment_method),
    nullif(btrim(coalesce(p_payment_reference, '')), ''),
    nullif(btrim(coalesce(p_bank_account, '')), ''),
    nullif(btrim(coalesce(p_notes, '')), ''), v_user_id
  ) returning id into v_payment_id;

  for v_allocation in select value from jsonb_array_elements(coalesce(p_allocations, '[]'::jsonb))
  loop
    begin
      v_bill_id := (v_allocation ->> 'bill_id')::bigint;
      v_amount := round((v_allocation ->> 'amount')::numeric, 2);
    exception when others then
      raise exception 'Every allocation requires a valid bill and amount.';
    end;
    if v_amount <= 0 then raise exception 'Every allocation amount must be greater than zero.'; end if;

    select * into v_bill from public.supplier_bills where id = v_bill_id for update;
    if not found then raise exception 'Supplier bill % was not found.', v_bill_id; end if;
    if v_bill.company_id <> p_company_id or v_bill.supplier_id <> p_supplier_id then
      raise exception 'Bill % belongs to another company or supplier.', coalesce(v_bill.bill_no, v_bill.id::text);
    end if;
    if upper(v_bill.currency) <> upper(coalesce(p_currency, v_supplier.default_currency, 'SGD')) then
      raise exception 'Bill % uses a different currency.', coalesce(v_bill.bill_no, v_bill.id::text);
    end if;
    if v_bill.status = 'cancelled' then raise exception 'Cancelled bill % cannot receive payment.', coalesce(v_bill.bill_no, v_bill.id::text); end if;

    v_balance := greatest(v_bill.total_amount - v_bill.amount_paid - v_bill.credit_applied, 0);
    if v_amount > v_balance + 0.004 then
      raise exception 'Allocation for bill % exceeds its outstanding balance of %.',
        coalesce(v_bill.bill_no, v_bill.id::text), to_char(v_balance, 'FM9999999990.00');
    end if;

    v_allocated := v_allocated + v_amount;
    if v_allocated > round(p_amount, 2) + 0.004 then raise exception 'Bill allocations exceed the payment amount.'; end if;

    insert into public.supplier_payment_allocations(payment_id, bill_id, allocated_amount)
    values (v_payment_id, v_bill_id, v_amount);
  end loop;

  update public.supplier_payments
  set allocated_amount = round(v_allocated, 2),
      unallocated_amount = round(p_amount - v_allocated, 2),
      updated_at = now()
  where id = v_payment_id;

  for v_bill_id in select distinct bill_id from public.supplier_payment_allocations where payment_id = v_payment_id
  loop perform public.refresh_supplier_bill_state(v_bill_id); end loop;

  return (
    select jsonb_build_object(
      'success', true, 'payment_id', payment.id, 'voucher_no', payment.voucher_no,
      'amount', payment.amount, 'allocated_amount', payment.allocated_amount,
      'unallocated_amount', payment.unallocated_amount
    ) from public.supplier_payments payment where payment.id = v_payment_id
  );
end;
$$;

create or replace function public.allocate_supplier_payment_credit(
  p_payment_id bigint,
  p_allocations jsonb
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid := auth.uid();
  v_payment public.supplier_payments%rowtype;
  v_allocation jsonb;
  v_bill public.supplier_bills%rowtype;
  v_bill_id bigint;
  v_amount numeric(14,2);
  v_applied numeric(14,2) := 0;
  v_balance numeric(14,2);
begin
  if v_user_id is null then raise exception 'Authentication is required.'; end if;
  if not public.is_app_administrator() then raise exception 'Administrator access is required.'; end if;

  select * into v_payment from public.supplier_payments where id = p_payment_id for update;
  if not found then raise exception 'Supplier payment was not found.'; end if;
  if v_payment.status <> 'posted' then raise exception 'Only a posted payment can be allocated.'; end if;
  if v_payment.unallocated_amount <= 0 then raise exception 'This payment has no unallocated amount.'; end if;

  for v_allocation in select value from jsonb_array_elements(coalesce(p_allocations, '[]'::jsonb))
  loop
    v_bill_id := (v_allocation ->> 'bill_id')::bigint;
    v_amount := round((v_allocation ->> 'amount')::numeric, 2);
    if v_amount <= 0 then raise exception 'Every allocation amount must be greater than zero.'; end if;

    select * into v_bill from public.supplier_bills where id = v_bill_id for update;
    if not found then raise exception 'Supplier bill % was not found.', v_bill_id; end if;
    if v_bill.company_id <> v_payment.company_id or v_bill.supplier_id <> v_payment.supplier_id then
      raise exception 'Bill % belongs to another company or supplier.', coalesce(v_bill.bill_no, v_bill.id::text);
    end if;
    if upper(v_bill.currency) <> upper(v_payment.currency) then raise exception 'Bill % uses a different currency.', coalesce(v_bill.bill_no, v_bill.id::text); end if;
    if v_bill.status = 'cancelled' then raise exception 'Cancelled bill % cannot receive payment.', coalesce(v_bill.bill_no, v_bill.id::text); end if;

    v_balance := greatest(v_bill.total_amount - v_bill.amount_paid - v_bill.credit_applied, 0);
    if v_amount > v_balance + 0.004 then raise exception 'Allocation exceeds bill % balance.', coalesce(v_bill.bill_no, v_bill.id::text); end if;

    v_applied := v_applied + v_amount;
    if v_applied > v_payment.unallocated_amount + 0.004 then raise exception 'Allocations exceed available unallocated payment.'; end if;

    insert into public.supplier_payment_allocations(payment_id, bill_id, allocated_amount)
    values (v_payment.id, v_bill_id, v_amount)
    on conflict(payment_id, bill_id)
    do update set allocated_amount = public.supplier_payment_allocations.allocated_amount + excluded.allocated_amount;
  end loop;

  update public.supplier_payments
  set allocated_amount = round(allocated_amount + v_applied, 2),
      unallocated_amount = round(unallocated_amount - v_applied, 2),
      updated_at = now()
  where id = v_payment.id;

  for v_bill_id in select distinct bill_id from public.supplier_payment_allocations where payment_id = v_payment.id
  loop perform public.refresh_supplier_bill_state(v_bill_id); end loop;

  return jsonb_build_object('success', true, 'payment_id', v_payment.id, 'voucher_no', v_payment.voucher_no);
end;
$$;

