create or replace function public.prepare_supplier_bill()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.currency := upper(coalesce(nullif(btrim(new.currency), ''), 'SGD'));
  new.subtotal := round(greatest(coalesce(new.subtotal, 0), 0), 2);
  new.gst_amount := round(greatest(coalesce(new.gst_amount, 0), 0), 2);
  new.total_amount := round(greatest(coalesce(new.total_amount, new.subtotal + new.gst_amount), 0), 2);
  if new.total_amount = 0 and new.subtotal + new.gst_amount > 0 then
    new.total_amount := round(new.subtotal + new.gst_amount, 2);
  end if;
  if new.bill_no is null or btrim(new.bill_no) = '' then
    new.bill_no := 'BILL-' || to_char(coalesce(new.bill_date, current_date), 'YYYYMMDD') || '-' || lpad(new.id::text, 5, '0');
  end if;
  new.updated_at := now();
  return new;
end;
$$;

