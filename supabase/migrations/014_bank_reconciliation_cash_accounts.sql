create or replace function public.prepare_bank_account()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.account_name := coalesce(nullif(btrim(new.account_name), ''), nullif(btrim(new.bank_name), ''), 'Bank Account');
  new.currency := upper(coalesce(nullif(btrim(new.currency), ''), 'SGD'));
  new.opening_balance := round(coalesce(new.opening_balance, 0), 2);
  new.is_active := coalesce(new.is_active, true);
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists bank_accounts_prepare_record on public.bank_accounts;
create trigger bank_accounts_prepare_record
before insert or update on public.bank_accounts
for each row execute function public.prepare_bank_account();

create or replace function public.prepare_bank_statement_batch()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if new.batch_no is null or btrim(new.batch_no) = '' then
    new.batch_no := 'BANK-' || to_char(coalesce(new.period_to, current_date), 'YYYYMMDD') || '-' || lpad(new.id::text, 5, '0');
  end if;
  new.opening_balance := round(coalesce(new.opening_balance, 0), 2);
  new.closing_balance := round(coalesce(new.closing_balance, 0), 2);
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists bank_statement_batches_prepare_record on public.bank_statement_batches;
create trigger bank_statement_batches_prepare_record
before insert or update on public.bank_statement_batches
for each row execute function public.prepare_bank_statement_batch();

