create table if not exists public.bank_reconciliation_matches (
  id bigint generated by default as identity primary key,
  statement_line_id bigint not null references public.bank_statement_lines(id) on delete cascade,
  cashbook_entry_id bigint not null references public.bank_cashbook_entries(id) on delete restrict,
  matched_amount numeric(16,2) not null,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  check (matched_amount > 0),
  unique(statement_line_id, cashbook_entry_id)
);

alter table public.bank_reconciliation_matches
  add column if not exists statement_line_id bigint references public.bank_statement_lines(id) on delete cascade,
  add column if not exists cashbook_entry_id bigint references public.bank_cashbook_entries(id) on delete restrict,
  add column if not exists matched_amount numeric(16,2) default 0,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now();

update public.bank_reconciliation_matches
set matched_amount = greatest(coalesce(matched_amount, 0), 0),
    created_at = coalesce(created_at, now());

create unique index if not exists bank_reconciliation_match_pair_uidx
  on public.bank_reconciliation_matches(statement_line_id, cashbook_entry_id);
create index if not exists bank_reconciliation_matches_line_idx
  on public.bank_reconciliation_matches(statement_line_id);
create index if not exists bank_reconciliation_matches_entry_idx
  on public.bank_reconciliation_matches(cashbook_entry_id);

