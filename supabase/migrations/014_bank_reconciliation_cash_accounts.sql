create or replace function public.refresh_bank_statement_line_state(p_statement_line_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_line public.bank_statement_lines%rowtype;
  v_matched numeric(16,2) := 0;
begin
  select * into v_line
  from public.bank_statement_lines
  where id = p_statement_line_id
  for update;

  if not found or v_line.match_status = 'ignored' then
    return;
  end if;

  select coalesce(sum(match.matched_amount), 0)
  into v_matched
  from public.bank_reconciliation_matches match
  join public.bank_cashbook_entries entry on entry.id = match.cashbook_entry_id
  where match.statement_line_id = p_statement_line_id
    and entry.status = 'posted';

  update public.bank_statement_lines
  set match_status = case
        when v_matched <= 0 then 'unmatched'
        when v_matched + 0.005 >= abs(v_line.amount) then 'matched'
        else 'partial'
      end,
      updated_at = now()
  where id = p_statement_line_id;
end;
$$;

create or replace function public.a3_match_bank_transaction(
  p_actor uuid,
  p_statement_line_id bigint,
  p_cashbook_entry_id bigint,
  p_amount numeric default null
)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  v_line public.bank_statement_lines%rowtype;
  v_entry public.bank_cashbook_entries%rowtype;
  v_batch public.bank_statement_batches%rowtype;
  v_line_used numeric(16,2) := 0;
  v_entry_used numeric(16,2) := 0;
  v_amount numeric(16,2);
  v_match_id bigint;
begin
  if not exists (
    select 1 from public.profiles
    where id = p_actor and role = 'administrator' and status = 'active'
  ) then
    raise exception 'Administrator access is required.';
  end if;

  select * into v_line from public.bank_statement_lines where id = p_statement_line_id for update;
  if not found then raise exception 'Bank statement line was not found.'; end if;

  select * into v_entry from public.bank_cashbook_entries where id = p_cashbook_entry_id for update;
  if not found then raise exception 'Cash-book entry was not found.'; end if;

  select * into v_batch from public.bank_statement_batches where id = v_line.batch_id for update;
  if v_batch.status = 'reconciled' then raise exception 'Reopen the reconciliation before changing matches.'; end if;
  if v_line.match_status = 'ignored' then raise exception 'Ignored statement lines cannot be matched.'; end if;
  if v_entry.status <> 'posted' then raise exception 'Only posted cash-book entries can be matched.'; end if;
  if v_line.company_id <> v_entry.company_id or v_line.bank_account_id <> v_entry.bank_account_id then
    raise exception 'The statement line and cash-book entry must use the same company and bank account.';
  end if;
  if sign(v_line.amount) <> sign(v_entry.amount) then
    raise exception 'Receipt and payment directions do not match.';
  end if;

  select coalesce(sum(matched_amount), 0) into v_line_used
  from public.bank_reconciliation_matches where statement_line_id = v_line.id;
  select coalesce(sum(matched_amount), 0) into v_entry_used
  from public.bank_reconciliation_matches where cashbook_entry_id = v_entry.id;

  v_amount := round(coalesce(
    p_amount,
    least(abs(v_line.amount) - v_line_used, abs(v_entry.amount) - v_entry_used)
  ), 2);

  if v_amount <= 0 then raise exception 'No unmatched amount remains.'; end if;
  if v_amount > abs(v_line.amount) - v_line_used + 0.005 then raise exception 'Match exceeds the remaining statement amount.'; end if;
  if v_amount > abs(v_entry.amount) - v_entry_used + 0.005 then raise exception 'Match exceeds the remaining cash-book amount.'; end if;

  insert into public.bank_reconciliation_matches(
    statement_line_id, cashbook_entry_id, matched_amount, created_by
  ) values (
    v_line.id, v_entry.id, v_amount, p_actor
  )
  on conflict (statement_line_id, cashbook_entry_id)
  do update set matched_amount = public.bank_reconciliation_matches.matched_amount + excluded.matched_amount
  returning id into v_match_id;

  perform public.refresh_bank_statement_line_state(v_line.id);
  return v_match_id;
end;
$$;

create or replace function public.a3_remove_bank_match(
  p_actor uuid,
  p_match_id bigint
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_line_id bigint;
  v_batch_status text;
begin
  if not exists (
    select 1 from public.profiles
    where id = p_actor and role = 'administrator' and status = 'active'
  ) then raise exception 'Administrator access is required.'; end if;

  select match.statement_line_id, batch.status
  into v_line_id, v_batch_status
  from public.bank_reconciliation_matches match
  join public.bank_statement_lines line on line.id = match.statement_line_id
  join public.bank_statement_batches batch on batch.id = line.batch_id
  where match.id = p_match_id;

  if v_line_id is null then return false; end if;
  if v_batch_status = 'reconciled' then raise exception 'Reopen the reconciliation before removing matches.'; end if;

  delete from public.bank_reconciliation_matches where id = p_match_id;
  perform public.refresh_bank_statement_line_state(v_line_id);
  return true;
end;
$$;

create or replace function public.a3_close_bank_reconciliation(
  p_actor uuid,
  p_batch_id bigint
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_batch public.bank_statement_batches%rowtype;
  v_statement_total numeric(16,2) := 0;
  v_calculated_closing numeric(16,2) := 0;
  v_unresolved integer := 0;
begin
  if not exists (
    select 1 from public.profiles
    where id = p_actor and role = 'administrator' and status = 'active'
  ) then raise exception 'Administrator access is required.'; end if;

  select * into v_batch from public.bank_statement_batches where id = p_batch_id for update;
  if not found then raise exception 'Reconciliation batch was not found.'; end if;

  select coalesce(sum(amount), 0),
         count(*) filter (where match_status not in ('matched','ignored'))
  into v_statement_total, v_unresolved
  from public.bank_statement_lines
  where batch_id = p_batch_id;

  v_calculated_closing := round(v_batch.opening_balance + v_statement_total, 2);

  if abs(v_calculated_closing - v_batch.closing_balance) > 0.02 then
    raise exception 'Statement opening balance plus transactions does not equal the closing balance.';
  end if;
  if v_unresolved > 0 then
    raise exception '% statement line(s) are still unmatched or partially matched.', v_unresolved;
  end if;

  update public.bank_statement_batches
  set status = 'reconciled', reconciled_by = p_actor, reconciled_at = now(), updated_at = now()
  where id = p_batch_id;

  return jsonb_build_object(
    'batch_id', p_batch_id,
    'calculated_closing_balance', v_calculated_closing,
    'statement_closing_balance', v_batch.closing_balance,
    'unresolved_lines', v_unresolved
  );
end;
$$;

create or replace function public.a3_reopen_bank_reconciliation(
  p_actor uuid,
  p_batch_id bigint
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
begin
  if not exists (
    select 1 from public.profiles
    where id = p_actor and role = 'administrator' and status = 'active'
  ) then raise exception 'Administrator access is required.'; end if;

  update public.bank_statement_batches
  set status = 'draft', reconciled_by = null, reconciled_at = null, updated_at = now()
  where id = p_batch_id;
  return found;
end;
$$;

