-- 3. MONTHLY BUDGETS
-- -----------------------------------------------------------------------------
create table if not exists public.pl_budgets (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  account_id bigint not null references public.pl_accounts(id) on delete cascade,
  budget_month date not null,
  amount numeric(16,2) not null default 0,
  notes text,
  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (amount >= 0),
  check (budget_month = date_trunc('month', budget_month)::date)
);

alter table public.pl_budgets
  add column if not exists company_id bigint references public.companies(id) on delete cascade,
  add column if not exists account_id bigint references public.pl_accounts(id) on delete cascade,
  add column if not exists budget_month date,
  add column if not exists amount numeric(16,2) default 0,
  add column if not exists notes text,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists updated_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

update public.pl_budgets
set budget_month = date_trunc('month', coalesce(budget_month, current_date))::date,
    amount = greatest(coalesce(amount, 0), 0),
    created_at = coalesce(created_at, now()),
    updated_at = coalesce(updated_at, now());

delete from public.pl_budgets duplicate
using public.pl_budgets original
where duplicate.company_id = original.company_id
  and duplicate.account_id = original.account_id
  and duplicate.budget_month = original.budget_month
  and duplicate.id > original.id;

create unique index if not exists pl_budgets_account_month_uidx
  on public.pl_budgets(company_id, account_id, budget_month);
create index if not exists pl_budgets_company_month_idx
  on public.pl_budgets(company_id, budget_month, account_id);

create or replace function public.prepare_pl_budget()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  account_company_id bigint;
begin
  new.budget_month := date_trunc('month', coalesce(new.budget_month, current_date))::date;
  new.amount := greatest(coalesce(new.amount, 0), 0);
  select company_id into account_company_id from public.pl_accounts where id = new.account_id;
  if account_company_id is null or account_company_id <> new.company_id then
    raise exception 'The budget account must belong to the same company.';
  end if;
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists pl_budgets_prepare on public.pl_budgets;
create trigger pl_budgets_prepare
before insert or update on public.pl_budgets
for each row execute function public.prepare_pl_budget();

