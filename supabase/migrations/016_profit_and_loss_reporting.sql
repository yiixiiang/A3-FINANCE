create or replace function public.seed_pl_defaults(p_company_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  sales_id bigint;
  service_id bigint;
  general_expense_id bigint;
  driver_cost_id bigint;
  bank_charge_id bigint;
  other_income_id bigint;
  interest_income_id bigint;
  other_expense_id bigint;
begin
  insert into public.pl_accounts(company_id, account_code, account_name, account_group, is_system, sort_order, description)
  values
    (p_company_id, '4000', 'Sales Revenue', 'revenue', true, 100, 'Core customer sales and service revenue.'),
    (p_company_id, '4100', 'Service Charge Revenue', 'revenue', true, 110, 'Service charge income before GST.'),
    (p_company_id, '5000', 'Cost of Sales', 'cost_of_sales', true, 200, 'Direct costs attributable to sales.'),
    (p_company_id, '5100', 'Driver Costs', 'cost_of_sales', true, 210, 'Approved driver payouts and related direct driver costs.'),
    (p_company_id, '6000', 'General Operating Expenses', 'operating_expense', true, 300, 'General supplier and operating expenses.'),
    (p_company_id, '6100', 'Vehicle Expenses', 'operating_expense', true, 310, 'Fuel, maintenance, parking, ERP and vehicle expenses.'),
    (p_company_id, '6200', 'Staff Costs', 'operating_expense', true, 320, 'Salary, CPF and employment costs.'),
    (p_company_id, '6300', 'Rent and Premises', 'operating_expense', true, 330, 'Rent, utilities and premises costs.'),
    (p_company_id, '6400', 'Marketing and Promotion', 'operating_expense', true, 340, 'Advertising, promotions and marketing costs.'),
    (p_company_id, '6500', 'Professional Fees', 'operating_expense', true, 350, 'Accounting, legal and professional services.'),
    (p_company_id, '6600', 'Bank Charges', 'operating_expense', true, 360, 'Bank fees and payment processing charges.'),
    (p_company_id, '7000', 'Other Income', 'other_income', true, 400, 'Non-core income and positive adjustments.'),
    (p_company_id, '7100', 'Interest Income', 'other_income', true, 410, 'Interest earned.'),
    (p_company_id, '8000', 'Other Expenses', 'other_expense', true, 500, 'Non-core expenses and negative adjustments.')
  on conflict (company_id, account_code) do update
    set account_name = excluded.account_name,
        account_group = excluded.account_group,
        is_system = true,
        sort_order = excluded.sort_order,
        description = excluded.description,
        updated_at = now();

  select id into sales_id from public.pl_accounts where company_id = p_company_id and account_code = '4000';
  select id into service_id from public.pl_accounts where company_id = p_company_id and account_code = '4100';
  select id into general_expense_id from public.pl_accounts where company_id = p_company_id and account_code = '6000';
  select id into driver_cost_id from public.pl_accounts where company_id = p_company_id and account_code = '5100';
  select id into bank_charge_id from public.pl_accounts where company_id = p_company_id and account_code = '6600';
  select id into other_income_id from public.pl_accounts where company_id = p_company_id and account_code = '7000';
  select id into interest_income_id from public.pl_accounts where company_id = p_company_id and account_code = '7100';
  select id into other_expense_id from public.pl_accounts where company_id = p_company_id and account_code = '8000';

  insert into public.pl_source_mappings(company_id, source_type, source_category, account_id, priority, is_active)
  values
    (p_company_id, 'customer_invoice', 'invoice_subtotal', sales_id, 10, true),
    (p_company_id, 'customer_invoice', 'service_charge', service_id, 10, true),
    (p_company_id, 'customer_invoice', '*', sales_id, 100, true),
    (p_company_id, 'supplier_bill', '*', general_expense_id, 100, true),
    (p_company_id, 'driver_payout', '*', driver_cost_id, 100, true),
    (p_company_id, 'bank_cashbook', 'bank_fee', bank_charge_id, 10, true),
    (p_company_id, 'bank_cashbook', 'interest_income', interest_income_id, 10, true),
    (p_company_id, 'bank_cashbook', 'interest_expense', other_expense_id, 10, true),
    (p_company_id, 'bank_cashbook', 'adjustment_income', other_income_id, 10, true),
    (p_company_id, 'bank_cashbook', 'adjustment_expense', other_expense_id, 10, true)
  on conflict (company_id, source_type, source_category) do update
    set account_id = excluded.account_id,
        priority = excluded.priority,
        is_active = true,
        updated_at = now();
end;
$$;

create or replace function public.seed_pl_defaults_for_new_company()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  perform public.seed_pl_defaults(new.id);
  return new;
end;
$$;

drop trigger if exists companies_seed_pl_defaults on public.companies;
