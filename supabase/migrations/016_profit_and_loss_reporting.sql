-- -----------------------------------------------------------------------------
-- 1. PROFIT & LOSS CHART OF ACCOUNTS
-- -----------------------------------------------------------------------------
create table if not exists public.pl_accounts (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  account_code text not null,
  account_name text not null,
  account_group text not null default 'operating_expense',
  normal_side text not null default 'debit',
  is_system boolean not null default false,
  is_active boolean not null default true,
  sort_order integer not null default 100,
  description text,
  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (account_group in ('revenue','cost_of_sales','operating_expense','other_income','other_expense')),
  check (normal_side in ('debit','credit'))
);

alter table public.pl_accounts
  add column if not exists company_id bigint references public.companies(id) on delete cascade,
  add column if not exists account_code text,
  add column if not exists account_name text,
  add column if not exists account_group text default 'operating_expense',
  add column if not exists normal_side text default 'debit',
  add column if not exists is_system boolean default false,
  add column if not exists is_active boolean default true,
  add column if not exists sort_order integer default 100,
  add column if not exists description text,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists updated_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

update public.pl_accounts
set account_code = upper(coalesce(nullif(btrim(account_code), ''), 'PL-' || id::text)),
    account_name = coalesce(nullif(btrim(account_name), ''), 'P&L Account ' || id::text),
    account_group = case
      when lower(coalesce(account_group, '')) in ('revenue','cost_of_sales','operating_expense','other_income','other_expense')
        then lower(account_group)
      else 'operating_expense'
    end,
    normal_side = case when lower(coalesce(normal_side, 'debit')) = 'credit' then 'credit' else 'debit' end,
    is_system = coalesce(is_system, false),
    is_active = coalesce(is_active, true),
    sort_order = coalesce(sort_order, 100),
    created_at = coalesce(created_at, now()),
    updated_at = coalesce(updated_at, now());

update public.pl_accounts duplicate
set account_code = duplicate.account_code || '-DUP-' || duplicate.id::text
where exists (
  select 1
  from public.pl_accounts original
  where original.company_id = duplicate.company_id
    and upper(original.account_code) = upper(duplicate.account_code)
    and original.id < duplicate.id
);

create unique index if not exists pl_accounts_company_code_uidx
  on public.pl_accounts(company_id, account_code);
create index if not exists pl_accounts_company_group_idx
  on public.pl_accounts(company_id, account_group, sort_order, account_code);

create or replace function public.prepare_pl_account()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.account_code := upper(coalesce(nullif(btrim(new.account_code), ''), 'PL'));
  new.account_name := coalesce(nullif(btrim(new.account_name), ''), 'P&L Account');
  new.account_group := lower(coalesce(nullif(btrim(new.account_group), ''), 'operating_expense'));
  if new.account_group not in ('revenue','cost_of_sales','operating_expense','other_income','other_expense') then
    raise exception 'Invalid P&L account group.';
  end if;
  new.normal_side := case
    when new.account_group in ('revenue','other_income') then 'credit'
    else 'debit'
  end;
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists pl_accounts_prepare on public.pl_accounts;
create trigger pl_accounts_prepare
before insert or update on public.pl_accounts
for each row execute function public.prepare_pl_account();

-- -----------------------------------------------------------------------------
-- 2. SOURCE-TO-ACCOUNT MAPPINGS
-- -----------------------------------------------------------------------------
create table if not exists public.pl_source_mappings (
  id bigint generated by default as identity primary key,
  company_id bigint not null references public.companies(id) on delete cascade,
  source_type text not null,
  source_category text not null default '*',
  account_id bigint not null references public.pl_accounts(id) on delete restrict,
  priority integer not null default 100,
  is_active boolean not null default true,
  notes text,
  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (source_type in ('customer_invoice','supplier_bill','driver_payout','bank_cashbook','manual'))
);

alter table public.pl_source_mappings
  add column if not exists company_id bigint references public.companies(id) on delete cascade,
  add column if not exists source_type text,
  add column if not exists source_category text default '*',
  add column if not exists account_id bigint references public.pl_accounts(id) on delete restrict,
  add column if not exists priority integer default 100,
  add column if not exists is_active boolean default true,
  add column if not exists notes text,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists updated_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

update public.pl_source_mappings
set source_type = case
      when lower(coalesce(source_type, '')) in ('customer_invoice','supplier_bill','driver_payout','bank_cashbook','manual')
        then lower(source_type)
      else 'manual'
    end,
    source_category = lower(coalesce(nullif(btrim(source_category), ''), '*')),
    priority = coalesce(priority, 100),
    is_active = coalesce(is_active, true),
    created_at = coalesce(created_at, now()),
    updated_at = coalesce(updated_at, now());

delete from public.pl_source_mappings duplicate
using public.pl_source_mappings original
where duplicate.company_id = original.company_id
  and duplicate.source_type = original.source_type
  and duplicate.source_category = original.source_category
  and duplicate.id > original.id;

create unique index if not exists pl_source_mappings_company_source_uidx
  on public.pl_source_mappings(company_id, source_type, source_category);
create index if not exists pl_source_mappings_lookup_idx
  on public.pl_source_mappings(company_id, source_type, is_active, priority, source_category);

create or replace function public.prepare_pl_source_mapping()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  account_company_id bigint;
begin
  new.source_type := lower(coalesce(nullif(btrim(new.source_type), ''), 'manual'));
  new.source_category := lower(coalesce(nullif(btrim(new.source_category), ''), '*'));
  select company_id into account_company_id from public.pl_accounts where id = new.account_id;
  if account_company_id is null or account_company_id <> new.company_id then
    raise exception 'The P&L account must belong to the same company as the mapping.';
  end if;
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists pl_source_mappings_prepare on public.pl_source_mappings;
create trigger pl_source_mappings_prepare
before insert or update on public.pl_source_mappings
for each row execute function public.prepare_pl_source_mapping();

