-- 4. MANUAL PROFIT & LOSS ENTRIES
-- -----------------------------------------------------------------------------
create table if not exists public.pl_manual_entries (
  id bigint generated by default as identity primary key,
  entry_no text,
  company_id bigint not null references public.companies(id) on delete cascade,
  account_id bigint not null references public.pl_accounts(id) on delete restrict,
  entry_date date not null default current_date,
  description text not null,
  reference text,
  direction text not null default 'increase',
  amount numeric(16,2) not null,
  status text not null default 'posted',
  notes text,
  void_reason text,
  voided_by uuid references public.profiles(id) on delete set null,
  voided_at timestamptz,
  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (direction in ('increase','decrease')),
  check (amount > 0),
  check (status in ('posted','void'))
);

alter table public.pl_manual_entries
  add column if not exists entry_no text,
  add column if not exists company_id bigint references public.companies(id) on delete cascade,
  add column if not exists account_id bigint references public.pl_accounts(id) on delete restrict,
  add column if not exists entry_date date default current_date,
  add column if not exists description text,
  add column if not exists reference text,
  add column if not exists direction text default 'increase',
  add column if not exists amount numeric(16,2) default 0,
  add column if not exists status text default 'posted',
  add column if not exists notes text,
  add column if not exists void_reason text,
  add column if not exists voided_by uuid references public.profiles(id) on delete set null,
  add column if not exists voided_at timestamptz,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists updated_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

update public.pl_manual_entries
set entry_no = coalesce(nullif(btrim(entry_no), ''), 'PLE-' || lpad(id::text, 6, '0')),
    entry_date = coalesce(entry_date, current_date),
    description = coalesce(nullif(btrim(description), ''), 'Manual P&L entry'),
    direction = case when lower(coalesce(direction, 'increase')) = 'decrease' then 'decrease' else 'increase' end,
    amount = greatest(abs(coalesce(amount, 0)), 0.01),
    status = case when lower(coalesce(status, 'posted')) = 'void' then 'void' else 'posted' end,
    created_at = coalesce(created_at, now()),
    updated_at = coalesce(updated_at, now());

update public.pl_manual_entries duplicate
set entry_no = duplicate.entry_no || '-DUP-' || duplicate.id::text
where exists (
  select 1
  from public.pl_manual_entries original
  where original.company_id = duplicate.company_id
    and lower(original.entry_no) = lower(duplicate.entry_no)
    and original.id < duplicate.id
);

create unique index if not exists pl_manual_entries_company_no_uidx
  on public.pl_manual_entries(company_id, entry_no);
create index if not exists pl_manual_entries_company_date_idx
  on public.pl_manual_entries(company_id, status, entry_date desc, id desc);

create or replace function public.prepare_pl_manual_entry()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  account_company_id bigint;
begin
  new.entry_date := coalesce(new.entry_date, current_date);
  new.description := coalesce(nullif(btrim(new.description), ''), 'Manual P&L entry');
  new.direction := case when lower(coalesce(new.direction, 'increase')) = 'decrease' then 'decrease' else 'increase' end;
  new.amount := abs(coalesce(new.amount, 0));
  if new.amount <= 0 then raise exception 'Manual P&L amount must be greater than zero.'; end if;
  select company_id into account_company_id from public.pl_accounts where id = new.account_id;
  if account_company_id is null or account_company_id <> new.company_id then
    raise exception 'The manual-entry account must belong to the same company.';
  end if;
  if new.entry_no is null or btrim(new.entry_no) = '' then
    new.entry_no := 'PLE-' || to_char(new.entry_date, 'YYYYMMDD') || '-' || lpad(new.id::text, 5, '0');
  end if;
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists pl_manual_entries_prepare on public.pl_manual_entries;
create trigger pl_manual_entries_prepare
before insert or update on public.pl_manual_entries
for each row execute function public.prepare_pl_manual_entry();

create or replace function public.void_pl_manual_entry(p_entry_id bigint, p_reason text)
returns public.pl_manual_entries
language plpgsql
security definer
set search_path = public
as $$
declare
  result public.pl_manual_entries;
begin
  if not public.is_app_administrator() then raise exception 'Administrator access is required.'; end if;
  if nullif(btrim(p_reason), '') is null then raise exception 'A void reason is required.'; end if;
  update public.pl_manual_entries
  set status = 'void',
      void_reason = btrim(p_reason),
      voided_by = auth.uid(),
      voided_at = now(),
      updated_by = auth.uid(),
      updated_at = now()
  where id = p_entry_id and status = 'posted'
  returning * into result;
  if result.id is null then raise exception 'Posted P&L entry was not found.'; end if;
  return result;
end;
$$;

