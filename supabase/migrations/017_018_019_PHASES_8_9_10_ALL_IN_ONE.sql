create or replace function public.prepare_finance_period()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if new.period_from is null or new.period_to is null then
    raise exception 'Financial period start and end dates are required.';
  end if;
  if new.period_to < new.period_from then
    raise exception 'Financial period end date cannot be earlier than the start date.';
  end if;
  if exists (
    select 1
    from public.finance_periods existing
    where existing.company_id = new.company_id
      and existing.id <> coalesce(new.id, 0)
      and daterange(existing.period_from, existing.period_to, '[]')
          && daterange(new.period_from, new.period_to, '[]')
  ) then
    raise exception 'The financial period overlaps an existing period for this company.';
  end if;
  if new.period_no is null or btrim(new.period_no) = '' then
    new.period_no := 'FP-' || to_char(new.period_from, 'YYYYMMDD') || '-' || to_char(new.period_to, 'YYYYMMDD');
  end if;
  new.period_name := coalesce(nullif(btrim(new.period_name), ''), to_char(new.period_from, 'Mon YYYY'));
  new.updated_at := now();
  return new;
end;
$$;

