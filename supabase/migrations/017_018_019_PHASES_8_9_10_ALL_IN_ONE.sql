create or replace function public.finance_role_can(p_module_name text, p_action text default 'view')
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce((
    select case lower(p_action)
      when 'view' then permission.can_view
      when 'create' then permission.can_create
      when 'edit' then permission.can_edit
      when 'approve' then permission.can_approve
      else false
    end
    from public.profiles profile
    join public.finance_module_permissions permission
      on permission.role_name = profile.role
     and permission.module_name = lower(p_module_name)
    where profile.id = auth.uid() and profile.status = 'active'
    limit 1
  ), false);
$$;

