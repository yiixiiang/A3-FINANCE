create or replace function public.prepare_bs_manual_entry()
returns trigger
language plpgsql
set search_path = public
as $$
declare
  account_company_id bigint;
begin
  new.entry_date := coalesce(new.entry_date, current_date);
  new.description := coalesce(nullif(btrim(new.description), ''), 'Balance Sheet entry');
  new.entry_side := case when lower(coalesce(new.entry_side, 'debit')) = 'credit' then 'credit' else 'debit' end;
  new.amount := abs(coalesce(new.amount, 0));
  if new.amount <= 0 then raise exception 'Balance Sheet entry amount must be greater than zero.'; end if;
  select company_id into account_company_id from public.bs_accounts where id = new.account_id;
  if account_company_id is null or account_company_id <> new.company_id then
    raise exception 'The Balance Sheet account must belong to the same company.';
  end if;
  if new.entry_no is null or btrim(new.entry_no) = '' then
    new.entry_no := 'BSE-' || to_char(new.entry_date, 'YYYYMMDD') || '-' || lpad(new.id::text, 5, '0');
  end if;
  new.updated_at := now();
  return new;
end;
$$;

