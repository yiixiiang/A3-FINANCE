create or replace function public.prepare_bs_account()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.account_code := upper(coalesce(nullif(btrim(new.account_code), ''), 'BS'));
  new.account_name := coalesce(nullif(btrim(new.account_name), ''), 'Balance Sheet Account');
  new.account_group := lower(coalesce(nullif(btrim(new.account_group), ''), 'current_asset'));
  if new.account_group not in ('current_asset','non_current_asset','current_liability','non_current_liability','equity') then
    raise exception 'Invalid Balance Sheet account group.';
  end if;
  new.is_contra := coalesce(new.is_contra, false);
  new.normal_side := case
    when new.account_group in ('current_liability','non_current_liability','equity')
      then case when new.is_contra then 'debit' else 'credit' end
    else case when new.is_contra then 'credit' else 'debit' end
  end;
  new.updated_at := now();
  return new;
end;
$$;

