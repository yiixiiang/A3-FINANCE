create or replace function public.void_bs_manual_entry(p_entry_id bigint, p_reason text)
returns public.bs_manual_entries
language plpgsql
security definer
set search_path = public
as $$
declare
  result public.bs_manual_entries;
begin
  if not public.is_app_administrator() then raise exception 'Administrator access is required.'; end if;
  if nullif(btrim(p_reason), '') is null then raise exception 'A void reason is required.'; end if;
  update public.bs_manual_entries
  set status = 'void', void_reason = btrim(p_reason), voided_by = auth.uid(), voided_at = now(),
      updated_by = auth.uid(), updated_at = now()
  where id = p_entry_id and status = 'posted'
  returning * into result;
  if result.id is null then raise exception 'Posted Balance Sheet entry was not found.'; end if;
  return result;
end;
$$;

