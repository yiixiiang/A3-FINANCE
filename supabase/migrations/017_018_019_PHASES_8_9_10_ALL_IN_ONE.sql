-- Generic period lock guard. The date-column name is supplied as trigger argument.
create or replace function public.guard_finance_period_lock()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  old_data jsonb;
  new_data jsonb;
  old_company bigint;
  new_company bigint;
  old_date date;
  new_date date;
begin
  if tg_op in ('UPDATE', 'DELETE') then
    old_data := to_jsonb(old);
    old_company := nullif(old_data ->> 'company_id', '')::bigint;
    old_date := nullif(old_data ->> tg_argv[0], '')::date;
    if old_company is not null and old_date is not null
       and public.is_finance_period_locked(old_company, old_date) then
      raise exception 'The financial period containing % is closed. Reopen the period before changing this record.', old_date;
    end if;
  end if;

  if tg_op in ('INSERT', 'UPDATE') then
    new_data := to_jsonb(new);
    new_company := nullif(new_data ->> 'company_id', '')::bigint;
    new_date := nullif(new_data ->> tg_argv[0], '')::date;
    if new_company is not null and new_date is not null
       and public.is_finance_period_locked(new_company, new_date) then
      raise exception 'The financial period containing % is closed. Reopen the period before changing this record.', new_date;
    end if;
  end if;

  if tg_op = 'DELETE' then return old; end if;
  return new;
end;
$$;

-- Install period lock guards only when the expected table/date column exists.
do $$
declare
  item record;
  trigger_name text;
begin
  for item in
    select * from (values
      ('customer_invoices','invoice_date'),
      ('customer_payments','payment_date'),
      ('supplier_bills','bill_date'),
      ('supplier_payments','payment_date'),
      ('supplier_credit_notes','credit_date'),
      ('driver_payouts','period_end'),
      ('bank_cashbook_entries','entry_date'),
      ('gst_adjustments','adjustment_date'),
      ('pl_manual_entries','entry_date'),
      ('bs_manual_entries','entry_date')
    ) as source(table_name, date_column)
  loop
    if to_regclass('public.' || item.table_name) is not null and exists (
      select 1 from information_schema.columns
      where table_schema = 'public' and table_name = item.table_name and column_name = item.date_column
    ) then
      trigger_name := item.table_name || '_finance_period_guard';
      execute format('drop trigger if exists %I on public.%I', trigger_name, item.table_name);
      execute format(
        'create trigger %I before insert or update or delete on public.%I for each row execute function public.guard_finance_period_lock(%L)',
        trigger_name, item.table_name, item.date_column
      );
    end if;
  end loop;
end $$;

