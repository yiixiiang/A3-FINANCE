create or replace function public.close_finance_period(
  p_period_id bigint,
  p_notes text default null
)
returns public.finance_periods
language plpgsql
security definer
set search_path = public
as $$
declare result public.finance_periods;
begin
  if not public.is_app_administrator() then raise exception 'Administrator access is required.'; end if;
  update public.finance_periods
  set status = 'closed', close_notes = nullif(btrim(p_notes), ''), closed_by = auth.uid(), closed_at = now(),
      reopened_by = null, reopened_at = null, reopened_reason = null, updated_at = now()
  where id = p_period_id and status = 'open'
  returning * into result;
  if result.id is null then raise exception 'Open finance period was not found.'; end if;
  insert into public.finance_audit_events(company_id,event_type,module_name,target_table,target_id,description,actor_user_id)
  values(result.company_id,'period_closed','financial_control','finance_periods',result.id::text,'Financial period closed.',auth.uid());
  return result;
end;
$$;

create or replace function public.reopen_finance_period(
  p_period_id bigint,
  p_reason text
)
returns public.finance_periods
language plpgsql
security definer
set search_path = public
as $$
declare result public.finance_periods;
begin
  if not public.is_app_administrator() then raise exception 'Administrator access is required.'; end if;
  if nullif(btrim(p_reason), '') is null then raise exception 'A reopen reason is required.'; end if;
  update public.finance_periods
  set status = 'open', reopened_by = auth.uid(), reopened_at = now(), reopened_reason = btrim(p_reason), updated_at = now()
  where id = p_period_id and status = 'closed'
  returning * into result;
  if result.id is null then raise exception 'Closed finance period was not found.'; end if;
  insert into public.finance_audit_events(company_id,event_type,module_name,target_table,target_id,description,details,actor_user_id)
  values(result.company_id,'period_reopened','financial_control','finance_periods',result.id::text,'Financial period reopened.',jsonb_build_object('reason',btrim(p_reason)),auth.uid());
  return result;
end;
$$;

