-- 1. FINANCIAL PERIOD CLOSE CONTROL
-- -----------------------------------------------------------------------------
create table if not exists public.finance_periods (
  id bigint generated by default as identity primary key,
  period_no text,
  company_id bigint not null references public.companies(id) on delete cascade,
  period_name text not null,
  period_from date not null,
  period_to date not null,
  status text not null default 'open',
  close_notes text,
  closed_by uuid references public.profiles(id) on delete set null,
  closed_at timestamptz,
  reopened_by uuid references public.profiles(id) on delete set null,
  reopened_at timestamptz,
  reopened_reason text,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (period_to >= period_from),
  check (status in ('open','closed')),
  unique(company_id, period_from, period_to)
);

alter table public.finance_periods
  add column if not exists period_no text,
  add column if not exists company_id bigint references public.companies(id) on delete cascade,
  add column if not exists period_name text,
  add column if not exists period_from date,
  add column if not exists period_to date,
  add column if not exists status text default 'open',
  add column if not exists close_notes text,
  add column if not exists closed_by uuid references public.profiles(id) on delete set null,
  add column if not exists closed_at timestamptz,
  add column if not exists reopened_by uuid references public.profiles(id) on delete set null,
  add column if not exists reopened_at timestamptz,
  add column if not exists reopened_reason text,
  add column if not exists created_by uuid references public.profiles(id) on delete set null,
  add column if not exists created_at timestamptz default now(),
  add column if not exists updated_at timestamptz default now();

create unique index if not exists finance_periods_company_range_uidx
  on public.finance_periods(company_id, period_from, period_to);
create index if not exists finance_periods_company_status_idx
  on public.finance_periods(company_id, status, period_to desc, id desc);

create or replace function public.prepare_finance_period()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  if new.period_from is null or new.period_to is null then
    raise exception 'Financial period start and end dates are required.';
  end if;
  if new.period_to < new.period_from then
    raise exception 'Financial period end date cannot be earlier than the start date.';
  end if;
  if exists (
    select 1
    from public.finance_periods existing
    where existing.company_id = new.company_id
      and existing.id <> coalesce(new.id, 0)
      and daterange(existing.period_from, existing.period_to, '[]')
          && daterange(new.period_from, new.period_to, '[]')
  ) then
    raise exception 'The financial period overlaps an existing period for this company.';
  end if;
  if new.period_no is null or btrim(new.period_no) = '' then
    new.period_no := 'FP-' || to_char(new.period_from, 'YYYYMMDD') || '-' || to_char(new.period_to, 'YYYYMMDD');
  end if;
  new.period_name := coalesce(nullif(btrim(new.period_name), ''), to_char(new.period_from, 'Mon YYYY'));
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists finance_periods_prepare on public.finance_periods;
create trigger finance_periods_prepare
before insert or update on public.finance_periods
for each row execute function public.prepare_finance_period();

create or replace function public.is_finance_period_locked(p_company_id bigint, p_transaction_date date)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.finance_periods
    where company_id = p_company_id
      and status = 'closed'
      and p_transaction_date between period_from and period_to
  );
$$;

-- Generic period lock guard. The date-column name is supplied as trigger argument.
create or replace function public.guard_finance_period_lock()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  old_data jsonb;
  new_data jsonb;
  old_company bigint;
  new_company bigint;
  old_date date;
  new_date date;
begin
  if tg_op in ('UPDATE', 'DELETE') then
    old_data := to_jsonb(old);
    old_company := nullif(old_data ->> 'company_id', '')::bigint;
    old_date := nullif(old_data ->> tg_argv[0], '')::date;
    if old_company is not null and old_date is not null
       and public.is_finance_period_locked(old_company, old_date) then
      raise exception 'The financial period containing % is closed. Reopen the period before changing this record.', old_date;
    end if;
  end if;

  if tg_op in ('INSERT', 'UPDATE') then
    new_data := to_jsonb(new);
    new_company := nullif(new_data ->> 'company_id', '')::bigint;
    new_date := nullif(new_data ->> tg_argv[0], '')::date;
    if new_company is not null and new_date is not null
       and public.is_finance_period_locked(new_company, new_date) then
      raise exception 'The financial period containing % is closed. Reopen the period before changing this record.', new_date;
    end if;
  end if;

  if tg_op = 'DELETE' then return old; end if;
  return new;
end;
$$;

-- Install period lock guards only when the expected table/date column exists.
do $$
declare
  item record;
  trigger_name text;
begin
  for item in
    select * from (values
      ('customer_invoices','invoice_date'),
      ('customer_payments','payment_date'),
      ('supplier_bills','bill_date'),
      ('supplier_payments','payment_date'),
      ('supplier_credit_notes','credit_date'),
      ('driver_payouts','period_end'),
      ('bank_cashbook_entries','entry_date'),
      ('gst_adjustments','adjustment_date'),
      ('pl_manual_entries','entry_date'),
      ('bs_manual_entries','entry_date')
    ) as source(table_name, date_column)
  loop
    if to_regclass('public.' || item.table_name) is not null and exists (
      select 1 from information_schema.columns
      where table_schema = 'public' and table_name = item.table_name and column_name = item.date_column
    ) then
      trigger_name := item.table_name || '_finance_period_guard';
      execute format('drop trigger if exists %I on public.%I', trigger_name, item.table_name);
      execute format(
        'create trigger %I before insert or update or delete on public.%I for each row execute function public.guard_finance_period_lock(%L)',
        trigger_name, item.table_name, item.date_column
      );
    end if;
  end loop;
end $$;

-- -----------------------------------------------------------------------------
-- 2. MODULE PERMISSIONS
-- -----------------------------------------------------------------------------
create table if not exists public.finance_module_permissions (
  id bigint generated by default as identity primary key,
  role_name text not null,
  module_name text not null,
  can_view boolean not null default false,
  can_create boolean not null default false,
  can_edit boolean not null default false,
  can_approve boolean not null default false,
  updated_by uuid references public.profiles(id) on delete set null,
  updated_at timestamptz not null default now(),
  unique(role_name, module_name),
  check (role_name in ('administrator','finance','viewer','user'))
);

alter table public.finance_module_permissions
  add column if not exists role_name text,
  add column if not exists module_name text,
  add column if not exists can_view boolean default false,
  add column if not exists can_create boolean default false,
  add column if not exists can_edit boolean default false,
  add column if not exists can_approve boolean default false,
  add column if not exists updated_by uuid references public.profiles(id) on delete set null,
  add column if not exists updated_at timestamptz default now();

insert into public.finance_module_permissions(role_name, module_name, can_view, can_create, can_edit, can_approve)
select role_name, module_name,
  role_name in ('administrator','finance','viewer'),
  role_name in ('administrator','finance'),
  role_name in ('administrator','finance'),
  role_name = 'administrator'
from unnest(array['administrator','finance','viewer','user']) as roles(role_name)
cross join unnest(array[
  'dashboard','invoices','receivables','payables','bank_reconciliation','gst_reports',
  'profit_loss','balance_sheet','cash_flow','financial_control'
]) as modules(module_name)
on conflict (role_name, module_name) do nothing;

update public.finance_module_permissions
set can_view = true, can_create = true, can_edit = true, can_approve = true, updated_at = now()
where role_name = 'administrator';

create or replace function public.finance_role_can(p_module_name text, p_action text default 'view')
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce((
    select case lower(p_action)
      when 'view' then permission.can_view
      when 'create' then permission.can_create
      when 'edit' then permission.can_edit
      when 'approve' then permission.can_approve
      else false
    end
    from public.profiles profile
    join public.finance_module_permissions permission
      on permission.role_name = profile.role
     and permission.module_name = lower(p_module_name)
    where profile.id = auth.uid() and profile.status = 'active'
    limit 1
  ), false);
$$;

